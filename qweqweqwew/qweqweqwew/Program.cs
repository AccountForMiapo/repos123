using System.Threading;
using System;
while (true)
{
    
    Console.Write("|");
    Thread.Sleep(200);
    Console.Clear();
    Console.Write("/");
    Thread.Sleep(200);
    Console.Clear();
    Console.Write("-");
    Thread.Sleep(200);
    Console.Clear();
    Console.Write("\\");
    Thread.Sleep(200);
    Console.Clear();
}